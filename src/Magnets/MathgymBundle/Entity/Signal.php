namespace Magnets\MathgymBundle\Entity;

use Doctrine\ORM\Mapping as ORM;

/**
 * Magnets\MathgymBundle\Entity\Signal
 *
 * @ORM\Table(name="signals")
 * @ORM\Entity(repositoryClass="Magnets\MathgymBundle\Entity\SignalRepository")
 */
class Signal
{

    /**
     * @ORM\Column(type="integer")
     * @ORM\Id
     * @ORM\GeneratedValue(strategy="AUTO")
     */
    private $id;

    /**
     * @ORM\ManyToOne(targetEntity="User")
     * @ORM\JoinColumn(name="user_id", referencedColumnName="id")
     */
    protected $user;

    /**
     * @ORM\Column(type="text")
     */
    private $payload;

    /**
     * @ORM\Column(type="string", length=255)
     */
    private $type;

    /**
     * @ORM\Column(type="datetime")
     */
    private $expires;

    public function __construct($user, $payload, $type, $expiry)
    {
        $this->setUser($user);
        $this->setPayload($payload);
        $this->setType($type);
        $this->setExpires($expiry);
    }

    /**
     * Get id
     *
     * @return integer
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set payload
     *
     * @param string $payload
     *
     * @return Signal
     */
    public function setPayload($payload)
    {
        $this->payload = $payload;

        return $this;
    }

    /**
     * Get payload
     *
     * @return string
     */
    public function getPayload()
    {
        return $this->payload;
    }

    /**
     * Set type
     *
     * @param string $type
     *
     * @return Signal
     */
    public function setType($type)
    {
        $this->type = $type;

        return $this;
    }

    /**
     * Get type
     *
     * @return string
     */
    public function getType()
    {
        return $this->type;
    }

    /**
     * Set expires
     *
     * @param \DateTime $expires
     *
     * @return Signal
     */
    public function setExpires($expires)
    {
        $this->expires = $expires;

        return $this;
    }

    /**
     * Get expires
     *
     * @return \DateTime
     */
    public function getExpires()
    {
        return $this->expires;
    }

    /**
     * Set user
     *
     * @param \Magnets\MathgymBundle\Entity\User $user
     *
     * @return Signal
     */
    public function setUser(\Magnets\MathgymBundle\Entity\User $user = null)
    {
        $this->user = $user;

        return $this;
    }

    /**
     * Get user
     *
     * @return \Magnets\MathgymBundle\Entity\User
     */
    public function getUser()
    {
        return $this->user;
    }
}
